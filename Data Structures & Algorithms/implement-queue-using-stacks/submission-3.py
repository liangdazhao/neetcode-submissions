class MyQueue:

    def __init__(self):
        self.stack_in = []
        self.stack_out = []

    def push(self, x: int) -> None:
        self.stack_in.append(x)

    def pop(self) -> int:
        return self.stack_in.pop(0)

    def peek(self) -> int:
        return self.stack_in[0]
        
    def empty(self) -> bool:
        return not self.stack_in


